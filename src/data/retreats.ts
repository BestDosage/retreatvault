function cs(score: number, notes: string = ""): CategoryScore {
  return {
    score,
    sub_scores: {},
    notes,
    data_sources: ["retreat_website", "google_reviews"],
    last_updated: "2026-03-01",
  };
}

function calcWrdScore(scores: RetreatScores): number {
  let total = 0;
  for (const [key, weight] of Object.entries(SCORE_WEIGHTS)) {
    total += scores[key as keyof RetreatScores].score * weight;
  }
  return Math.round(total * 10) / 10;
}

function makeRetreat(partial: Omit<WellnessRetreat, "wrd_score" | "score_tier" | "created_at" | "updated_at" | "last_data_refresh" | "recent_reviews" | "youtube_videos" | "gallery_images" | "is_sponsored">): WellnessRetreat {
  const wrd_score = calcWrdScore(partial.scores);
  return {
    ...partial,
    wrd_score,
    score_tier: getScoreTier(wrd_score),
    created_at: "2026-03-01",
    updated_at: "2026-03-01",
    last_data_refresh: "2026-03-01",
    recent_reviews: [],
    youtube_videos: [],
    gallery_images: [],
    is_sponsored: false,
  };
}

